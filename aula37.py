# Operadores relacionais
# > - Maior
# < - Menor
# >= - Maior ou igual
# <= - Menor ou igual
# == - Igual
# != - Diferente

print(f'2 é maior que 1?: {2 > 1}')
print(f'3 é menor que 5?: {3 < 5}')
print(f'4 é maior ou igual a 4?: {4 >= 4}')
print(f'8 é menor ou igual a 5?: {8 <= 5}')
print(f'5 é diferente de 5?: {5 != 5}')